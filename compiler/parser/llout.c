            break;
        case 527:
		        { make_term(yytext); }
            break;
        case 520:
		        { new_lev ('r'); }
            break;
        case 528:
		        { new_field(yytext); }
            break;
        case 521:
		        { new_field(""); }
            break;
        case 532:
		        { new_nonterm(yytext,1); }
            break;
        case 512:
		        { first_sym(); }
            break;
        case 529:
		        { prec_list(yytext); }
            break;
        case 533:
		        { new_nonterm(yytext,1); }
            break;
        case 513:
		        { ws(); }
            break;
        case 530:
		        { new_field(yytext); }
            break;
        case 534:
		        { new_rhs(); }
            break;
        case 514:
		        { union_def( yytext ); }
            break;
        case 531:
		        { add_synch(yytext); }
            break;
        case 535:
		        { add_to_rhs(yytext, 0             ); }
            break;
        case 522:
		        { new_lev ('n'); }
            break;
        case 515:
		        { new_field(""); }
            break;
        case 536:
		        { add_to_rhs(yytext, 0             ); }
            break;
        case 523:
		        { new_field(""); }
            break;
        case 516:
		        { new_lev ( 0 ); }
            break;
        case 537:
		        { add_to_rhs(yytext, start_action()); }
            break;
        case 524:
		        {
		     lerror(NONFATAL,"%%start not supported by occs. First " );
		     lerror(NOHDR,   "production is the start production\n." );
		  }
            break;
        case 517:
		        { new_field(""); }
            break;
        case 538:
		        { prec      (yytext                ); }
            break;
        case 525:
		        { new_nonterm (yytext,0); }
